export type TranslationKey = keyof typeof translations.en | (string & {});

export function getTranslation(locale: Locale, key: TranslationKey, replacements?: Record<string, string | number>): string {
  let text: string = (translations[locale] as any)[key] || (translations.en as any)[key] || key as string;
  
  if (replacements) {
    Object.entries(replacements).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, String(v));
    });
  }
  
  return text;
}
